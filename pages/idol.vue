      return this.artists
        .map(artist => ({
          ...artist,
          mainWorks: this.filterWorks(artist.mainWorks),
          compilations: this.filterWorks(artist.compilations)
        }))
        .filter(
          artist =>
            artist.mainWorks.length > 0 || artist.compilations.length > 0
        )
    }
